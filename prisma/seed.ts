async function main() {
  // Пользователи
  const usersByName = new Map<string, string>();
  for (const member of TEAM) {
    const user = await prisma.user.upsert({
      where: { email: member.email },
      update: { name: member.name },
      create: { name: member.name, email: member.email },
    });
    usersByName.set(member.name, user.id);
  }

  // Недели + отчёты
  for (const w of WEEKS) {
    const week = await prisma.week.upsert({
      where: { startDate: new Date(`${w.start}T00:00:00Z`) },
      update: { label: w.label, endDate: new Date(`${w.end}T00:00:00Z`) },
      create: {
        label: w.label,
        startDate: new Date(`${w.start}T00:00:00Z`),
        endDate: new Date(`${w.end}T00:00:00Z`),
      },
    });

    for (const [name, projects] of Object.entries(w.reports)) {
      const userId = usersByName.get(name);
      if (!userId) continue;

      const cleaned = projects.map((p, i) => ({
        name: p.name,
        done: p.done ?? "",
        blockers: p.blockers ?? "",
        plans: p.plans ?? "",
        order: i,
      }));

      await prisma.report.upsert({
        where: { userId_weekId: { userId, weekId: week.id } },
        update: { projects: { deleteMany: {}, create: cleaned } },
        create: { userId, weekId: week.id, projects: { create: cleaned } },
      });
    }
  }

  // Проекты-сущности: группируем строки отчётов по имени и проставляем projectId
  // (та же логика, что в prisma/backfill-projects.ts).
  const rows = await prisma.reportProject.findMany({
    where: { projectId: null },
    select: { id: true, name: true },
  });
  const groups = new Map<string, { display: string; ids: string[] }>();
  for (const row of rows) {
    const display = row.name.trim().replace(/\s+/g, " ");
    if (!display) continue;
    const key = display.toLowerCase();
    const group = groups.get(key) ?? { display, ids: [] };
    group.ids.push(row.id);
    groups.set(key, group);
  }
  for (const { display, ids } of groups.values()) {
    const existing = await prisma.project.findFirst({
      where: { name: { equals: display, mode: "insensitive" } },
      select: { id: true },
    });
    const project =
      existing ??
      (await prisma.project.create({
        data: { name: display },
        select: { id: true },
      }));
    await prisma.reportProject.updateMany({
      where: { id: { in: ids } },
      data: { projectId: project.id },
    });
  }

  console.log(
    `✅ Засеяно: ${TEAM.length} сотрудников, ${WEEKS.length} недели с отчётами, ${groups.size} проектов.`,
  );
}

main()
  .then(() => prisma.$disconnect())
  .catch(async (e) => {
    console.error(e);
    await prisma.$disconnect();
    process.exit(1);
  });
